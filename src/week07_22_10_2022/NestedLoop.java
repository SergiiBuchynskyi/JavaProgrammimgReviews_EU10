package week07_22_10_2022;

public class NestedLoop {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;

        for (int i = 0; i < a; i++) {
            System.out.println();
            for (int j = 0; j < b; j++) {
                System.out.print("*");

            }
        }









    }

}
